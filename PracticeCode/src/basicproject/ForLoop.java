package basicproject;

public class ForLoop {
	public static void main(String[] args) {
	for(int i=0;i<=10; i++) {
		System.out.println(2*i);
	}
	int n=5;
	for(int i=0;i<n;i++) {
		
		System.out.println(2*i+1);
		
	}
	for(int i=5;i>0;i--) {
		System.out.println(i);
	}
	for(int i=7;i!=0;i--) {
		System.out.println(i);
	}
	
for(int i=0;i<5;i++) {
	System.out.println();
	for(int j=0;j<=i;j++) {
		System.out.print("*");
		
	}	
}
System.out.println();
for(int i=0;i<5;i++) {
	System.out.println(" ");
	for(int j=0;j<5;j++) {
		System.out.print("*");
	}
}


	}
	
}
