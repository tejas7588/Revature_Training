package basicproject;

public class DoWhileLoop {
	public static void main(String[] args) {
		
		
		int a=0;
		while(a<5) {
			System.out.println(a);
			a++;
			System.out.println("successfully run...........");
		}
		int b=10;
		do {
			System.out.println(b);
			b++;
		}while(b<5);
		
		
		
	}

}
